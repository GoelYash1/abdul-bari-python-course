morse_code_list = [
    '.-', '-...', '-.-.', '-..', '.', '..-.', '--.', '....', '..', '.---',
    '-.-', '.-..', '--', '-.', '---', '.--.', '--.-', '.-.', '...', '-',
    '..-', '...-', '.--', '-..-', '-.--', '--..', '.----', '..---', '...--',
    '....-', '.....', '-....', '--...', '---..', '----.', '-----', '--..--',
    '.-.-.-', '..--..', '-..-.', '-....-', '-.--.', '-.--.-'
]

name = input("Enter your name: ")
ans = ""
for c in name.upper():
    if c.isspace():
        ans += "    "
    else:
        ans += morse_code_list[ord(c)-ord('A')]

print(ans)